package classperson;


public class Person {
    // Atribut
    String nama;
    int umur;
    String JenisKelamin;
    
    // Konstruktor
    public Person(String nama, int umur, String JenisKelamin) {
        this.nama = nama;
        this.umur = umur;
        this.JenisKelamin = JenisKelamin;
        
    }

    // getter dan setter untuk atribut nama
    public String getNama() {
        return nama;
        
    }

    public void setNama(String nama) {
        this.nama = nama;
        
    }
}

    // Getter dan Setter untuk atribut umur
    public int getUmur() {
        return umur;
    }

    public void setumur(int umur) {
        this.umur = umur;
    }

    


    


